// One-time tool: rasterize the shop logo (public/logo.svg) into a 1-bit
// ESC/POS raster image command (GS v 0) and embed it as a base64 constant.
//
// The thermal printer prints black dots only, so the colour logo is reduced
// to monochrome via Floyd–Steinberg dithering. Output is a fixed byte blob —
// the running app/bridge needs NO image library at runtime.
//
// Run:  node scripts/gen-receipt-logo.mjs   (from the app/ directory)
// Re-run whenever public/logo.svg changes, then paste the printed BRIDGE
// base64 into bridge/server.mjs.

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { Resvg } from '@resvg/resvg-js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const APP_ROOT = path.resolve(__dirname, '..');

// ── Tunables ───────────────────────────────────────────────────────
const TARGET_WIDTH = 200;   // dots; must be a multiple of 8. ~25mm on an 80mm head
const THRESHOLD = 128;      // 0–255 luminance cut for the dithering error diffusion
// ───────────────────────────────────────────────────────────────────

const svg = fs.readFileSync(path.join(APP_ROOT, 'public', 'logo.svg'));

const resvg = new Resvg(svg, {
  fitTo: { mode: 'width', value: TARGET_WIDTH },
  background: 'white', // flatten transparency to white so it doesn't print black
});
const img = resvg.render();
const { width, height } = img;
const rgba = img.pixels; // Uint8Array, RGBA row-major

if (width % 8 !== 0) {
  console.warn(`width ${width} is not a multiple of 8 — rows will be byte-padded.`);
}

// Luminance buffer (0 = black, 255 = white)
const lum = new Float32Array(width * height);
for (let i = 0; i < width * height; i++) {
  const r = rgba[i * 4], g = rgba[i * 4 + 1], b = rgba[i * 4 + 2];
  lum[i] = 0.299 * r + 0.587 * g + 0.114 * b;
}

// Floyd–Steinberg dithering → 1 (black) / 0 (white)
const mono = new Uint8Array(width * height);
for (let y = 0; y < height; y++) {
  for (let x = 0; x < width; x++) {
    const i = y * width + x;
    const old = lum[i];
    const black = old < THRESHOLD;
    mono[i] = black ? 1 : 0;
    const newVal = black ? 0 : 255;
    const err = old - newVal;
    const spread = (dx, dy, f) => {
      const nx = x + dx, ny = y + dy;
      if (nx < 0 || nx >= width || ny < 0 || ny >= height) return;
      lum[ny * width + nx] += (err * f) / 16;
    };
    spread(1, 0, 7);
    spread(-1, 1, 3);
    spread(0, 1, 5);
    spread(1, 1, 1);
  }
}

// Pack into ESC/POS GS v 0 raster: bits MSB-first, 1 = black dot
const bytesPerRow = Math.ceil(width / 8);
const raster = Buffer.alloc(bytesPerRow * height);
for (let y = 0; y < height; y++) {
  for (let x = 0; x < width; x++) {
    if (mono[y * width + x]) {
      raster[y * bytesPerRow + (x >> 3)] |= 0x80 >> (x & 7);
    }
  }
}

const header = Buffer.from([
  0x1d, 0x76, 0x30, 0x00,               // GS v 0  m=0 (normal)
  bytesPerRow & 0xff, (bytesPerRow >> 8) & 0xff,
  height & 0xff, (height >> 8) & 0xff,
]);
const command = Buffer.concat([header, raster]);
const b64 = command.toString('base64');

// Emit the shared TS module consumed by the Next.js print route
const tsPath = path.join(APP_ROOT, 'src', 'lib', 'receipt-logo.ts');
const ts = `// AUTO-GENERATED by scripts/gen-receipt-logo.mjs — do not edit by hand.
// Monochrome (${width}x${height}) ESC/POS GS v 0 raster of public/logo.svg.
// Re-run the script after changing the logo, then sync the base64 to bridge/server.mjs.

const RECEIPT_LOGO_ESCPOS_B64 =
  '${b64}';

/** Full ESC/POS GS v 0 raster command for the shop logo (centered by caller). */
export const receiptLogoCommand = (): Buffer =>
  Buffer.from(RECEIPT_LOGO_ESCPOS_B64, 'base64');
`;
fs.writeFileSync(tsPath, ts);

console.log(`rendered ${width}x${height}, command ${command.length} bytes`);
console.log(`wrote ${path.relative(APP_ROOT, tsPath)}`);
console.log('\n── paste this base64 into bridge/server.mjs (LOGO_ESCPOS_B64) ──');
console.log(b64);
